package com.iqr.domain;

public class Alarms {

	private int idx;
	private String qr_code;
	private String u_company;
	private String alarm_name;
	private String alarm_time;
	private String reset_time;
	private String alarm_sdate;
	private String yn;
	
	
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getQr_code() {
		return qr_code;
	}
	public void setQr_code(String qr_code) {
		this.qr_code = qr_code;
	}
	public String getU_company() {
		return u_company;
	}
	public void setU_company(String u_company) {
		this.u_company = u_company;
	}
	public String getAlarm_name() {
		return alarm_name;
	}
	public void setAlarm_name(String alarm_name) {
		this.alarm_name = alarm_name;
	}
	public String getAlarm_time() {
		return alarm_time;
	}
	public void setAlarm_time(String alarm_time) {
		this.alarm_time = alarm_time;
	}
	public String getAlarm_sdate() {
		return alarm_sdate;
	}
	public void setAlarm_sdate(String alarm_sdate) {
		this.alarm_sdate = alarm_sdate;
	}
	public String getReset_time() {
		return reset_time;
	}
	public void setReset_time(String reset_time) {
		this.reset_time = reset_time;
	}
	public String getYn() {
		return yn;
	}
	public void setYn(String yn) {
		this.yn = yn;
	}
	
	
	
}
